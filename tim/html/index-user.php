		<h2 class="tituloh2">Algumas de suas Teorias</h2>
		<div class="container-teorias">
			<div class="card-teorias">
				<h4>Cura através da Palavra</h4>
				<p>Em suma, ao contrário de outras abordagens da época, o paciente falaria o que viesse em sua mente enquanto o terapeuta somente ouviria. O método de associação livre consiste no paciente externar verbalmente o que vem na mente dele, incluindo suas angústias.</p>
			</div>
			<div class="card-teorias">
				<h4>Complexo de Édipo</h4>
				<p>Trata-se de um estágio na infância onde a criança se volta amorosamente para um dos pais enquanto rivaliza o outro. Entretanto, por volta dos 5 anos, a criança reequilibra seus laços e acolhe ambos os pais.</p>
			</div>
			<div class="card-teorias">
				<h4>Libido</h4>
				<p>Essa se mostra como uma energia sexual que se interliga diretamente com as funções físicas e mentais de uma pessoa. De acordo com Freud, por meio dela é que alcançamos um desenvolvimento mais completo.</p>
			</div>
		</div>
		<div class="container-teorias">
			<div class="card-teorias">
				<h4>Sonhos</h4>
				<p>Na Psicanálise de Sigmund Freud, principais teorias e ideias, os sonhos ocupam um posto no alto escalão. Sendo temas recorrentes da terapia, são passagens lineares para entender melhor nosso inconsciente. Assim que são traduzidos, podem indicar importantes revelações sobre nós e nossas vidas.</p>
			</div>
			<div class="card-teorias">
				<h4>Estruturação Mental</h4>
				<p>Na explicação das principais ideias de Sigmund Freud, a estruturação mental seria um resultado de desenvolvimento infantil. De acordo com isso, não há normalidade, mas, sim, estágios variados de perversão, psicopatia ou neurose.</p>
			</div>
			<div class="card-teorias">
				<h4>Sublimação</h4>
				<p>Sublimação é o direcionamento da libido para objetos que não estejam ligados com o sexo. Basicamente, você utiliza dessa força para construir algo bom em sua vida.</p>
