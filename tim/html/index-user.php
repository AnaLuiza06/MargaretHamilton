					<p>Sigmund Schlomo Freud nasceu no dia 06 de maio de 1856 em Pribor. Na época, a cidade formava parte do Império Austro-Húngaro e, atualmente, se encontra na República Tcheca.</p>
					<p>Na capital do Império, em 1873, ingressou no curso de Medicina na Universidade de Viena formando-se especialista em Fisiologia Nervosa, em 1882. Aperfeiçoou seus conhecimentos em Paris, onde estudou com Jean Charcot, médico dedicado ao estudo do tratamento da histeria através da hipnose.</p>
					<p>Casou-se, em 1886, com Martha Bernays, e com ela teve seis filhos: Mathilde, Jean-Martin, Olivier, Ernst, Sophie e Anna. Esta última seguiu os passos do pai e foi uma psicanalista de destaque. Publicou várias obras e em 1908, junto com seus seguidores Karl Abraham, Sandor Ferenczi e Ernest Jones, fundou a “Sociedade Psicanalítica de Viena”. Em 1938, fugiu para Londres com ajuda da princesa Maria Bonaparte (1882-1962), para escapar da perseguição imposta pelo Nazismo aos judeus. Quatro de suas irmãs morreram em campos de concentração.</p>
					<p>Freud sofreu de câncer na mandíbula, doença que o fez passar por mais de 30 cirurgias. Alguns pesquisadores, inclusive, afirmam que ele morreu por overdose de morfina, pois sentia muitas dores. Faleceu no dia 23 de setembro de 1939, em Londres deixando um novo campo de estudo para a humanidade.</p>

